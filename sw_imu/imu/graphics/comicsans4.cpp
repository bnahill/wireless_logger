bool ComicSans4::write_text(uint8_t * buff, uint8_t line, char const * text,
	                        uint32_t &n_cols, uint32_t max_cols){
	if(line >= n_lines)
		return false;
	
	uint8_t * const buff_orig = buff;
	
	uint8_t const * char_ptr;
	uint32_t i;
	char letter;
	
	max_cols -= char_width + letter_spacing;

	
	// Iterate over each character of text
	while(true){
		if((text[0] < 32) || (text[0] > 127))
			letter = ' ' - 32;
		else
			letter = text[0]-32;
		// First column of letter in font
		char_ptr = &font[(uint8_t)letter][line][0];
		
		// Copy letter data
		for(i = 0; i < char_width; i++){
 			buff[i] = char_ptr[i];
		}
		
		buff += char_width;
		
		
		// Check for exit before adding spacing
		if(*(++text) == 0)
			break;
		
		if((uint32_t)(buff - buff_orig) > max_cols)
			break;
		
		// Add spacing
		for(i = 0; i < letter_spacing; i++){
 			buff[i] = 0;
		}
		
		buff += letter_spacing;
	}
	
	n_cols = buff - buff_orig;
	return true;
}

uint32_t ComicSans4::get_num_cols(const char * text){
	uint8_t ret = 0;
	while(true){
		ret += char_width;
		if(*(++text) == 0)
			break;
		ret += letter_spacing;
	}
	return ret;
}

